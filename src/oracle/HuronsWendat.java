package oracle;

public class HuronsWendat extends Miracle {

    private String nomOracle;

    public HuronsWendat(String nomOracle) {
        this.nomOracle = nomOracle;
    }

    @Override
    public void realiserMiracle() {
        super.realiserMiracle();
    }

    @Override
    public String toString() {
        return "HuronsWendat{" +
                "nomOracle='" + nomOracle + '\'' +
                '}';
    }
}
