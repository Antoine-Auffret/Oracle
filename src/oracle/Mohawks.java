package oracle;

public class Mohawks extends Prophetie {

    private String nomOracle;

    public Mohawks(String nomOracle) {
        this.nomOracle = nomOracle;
    }

    @Override
    public void enoncerProphetie() {
        super.enoncerProphetie();
    }

    @Override
    public void predireProphetie() {
        super.predireProphetie();
    }

    @Override
    public void realiserProphetie() {
        super.realiserProphetie();
    }

    @Override
    public String toString() {
        return "Mohawks{" +
                "nomOracle='" + nomOracle + '\'' +
                '}';
    }
}
